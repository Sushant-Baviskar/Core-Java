package BasicJava;

public class CharToInt {

	public static void main(String[] args) {
		
		char ch = 'a',ch3 = 'z';
		char ch2 = 'A',ch4 = 'Z';
		
		
		int x = (int)ch;
		int y = ch2;
		int m = ch3;
		int n = ch4;
		
		System.out.println(x);
		System.out.println(y);
		System.out.println(m);
		System.out.println(n);
		

	}

}
