package BasicJava;

public class BitwiseAndShiftOperator {

	public static void main(String[] args) {
		int a=2, b=12;
		
		System.out.println(a & b);
		
		int x=8,y=12;
		System.out.println(x^y);
		System.out.println(x<<1);
		System.out.println(x>>>1);
		
		int z= -8;
		System.out.println(z>>1);

	}

}
